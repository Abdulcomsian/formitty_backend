<p style="font-weight: bold; margin-bottom: 10px; font-size: 11pt">There are specific templates available for the following types of AT:</p>
<ul style="margin-bottom: 10px; margin-top: 10px">
    <li style="font-size: 11pt">General Assistive Technology Assessment </li>
    <li style="font-size: 11pt">Continence Assessment </li>
    <li style="font-size: 11pt">Prosthetics and Orthotics Assessment</li>
    <li style="font-size: 11pt">Vehicle Modifications Assessment</li>
    <li style="font-size: 11pt">Complex Home Modification Assessment</li>
    <li style="font-size: 11pt">Dog Guide Assessment</li>
</ul>
<p style="font-size: 11pt; margin-bottom: 10px; margin-top: 10px">The assessment information provided in this form will be used by the NDIA to understand how the specified AT will assist the participant to pursue their goals and to assess whether it is reasonable and necessary for the NDIS to fund AT support.</p>
<p style="font-size: 11pt; margin-bottom: 10px">Using this template is not mandatory. If a provider elects to provide information in another format, they must include all information described in this template. Information provided needs to include an outline of the functional impact of each feature being recommended. This should include how the AT will support capacity building, promote independence and impact alternative forms of support. </p>
<p style="font-size: 11pt; margin-bottom: 10px">The primary criteria NDIS delegates use when determining if a piece of equipment or modification is suitable for the NDIS to fund is section 34: reasonable and necessary supports of the <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">National Disability Insurance Scheme Act 2013 (NDIS Act; external) and section 34.</a></p>
<p style="font-size: 11pt; margin-bottom: 10px">Additional information on how the recommendation(s) will be considered in the context of specific supports can be found in the <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">NDIS Operational Guidelines available online</a> and the <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">NDIS (Supports for Participants) Rules 2013.</a></p>
<p style="font-size: 11pt; margin-bottom: 10px"><strong>AT Strategy:</strong> Supports will be provided in line with the NDIA’s AT Strategy that can be found at <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">Assistive Technology Strategy</a> and as outlined in <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">NDIS AT Complexity</a> document.</p>
<p style="font-size: 11pt; margin-bottom: 10px"><strong>Assistive Technology (AT) Assessor:</strong> An AT assessor is able to assess a participant's needs and situation and identify the most appropriate AT, they may be an AT Mentor, allied health practitioner, continence nurse, rehabilitation engineer or other suitably qualified practitioner. </p>
<p style="font-size: 11pt; margin-bottom: 10px">AT Assessors have obligations under the NDIS Provider Terms of Business, Quality and Safeguards Commission and their respective professional registration under Australian Health Practitioner Regulation Agency (AHPRA).</p>
<p style="font-size: 11pt; margin-bottom: 10px"><strong>Caution:</strong> AT Assessors must be aware of and observe the law with regard to AT that is likely to restrain a participant. <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;"> National Disability Insurance Scheme (Restrictive Practices and Behaviour Support) Rules 2018</a> </p>
<p style="font-size: 11pt; margin-bottom: 10px">The NDIA expects AT assessors to consider all options for addressing the participant’s disability related functional limitations and pursuing goals, including non-AT supports.</p>
<p style="font-size: 11pt; margin-bottom: 10px"><strong>NDIS AT Levels 3 and 4 trials:</strong> Where the AT assessor and participant need to work with an AT supplier to trial and develop a specification for the AT support, reasonable supplier costs can be quoted, and if agreed, claimed against the participant’s plan (category ‘rental/trial’). Supplier specification/order details are required with this assessment to enable the NDIS to consider quotes/prices from the supplier.</p>
<p style="font-size: 11pt; margin-bottom: 10px">Quotations should be attached where applicable (items $1500 may be funded from the CORE consumables budget and do not require an assessment and quote). Quotations can include printouts of web orders and stock numbers from relevant State based equipment suppliers where relevant.</p>
<p style="font-size: 11pt; margin-bottom: 10px">AT assessors can keep up to date at <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">NDIS provider assistive technology</a>. Participants can keep up to date at <a href="http://phpword.readthedocs.io" style="text-decoration: underline; color: blue;">NDIS participant assistive technology</a>. </p>
